// ============ Carousel ============
let currentCarouselIndex = 0;
let carouselItems = [];

function initializeCarousel() {
    // Combine photos and videos for carousel
    carouselItems = [];
    
    // Alternate between photos and videos
    const maxItems = Math.max(photos.length, videos.length);
    for (let i = 0; i < maxItems; i++) {
        if (i % 2 === 0 && i < photos.length) {
            carouselItems.push({ type: 'photo', src: photos[i] });
        }
        if (i < videos.length) {
            carouselItems.push({ type: 'video', src: videos[i] });
        }
    }

    const carouselContainer = document.getElementById('carouselContainer');
    const carouselDots = document.getElementById('carouselDots');

    carouselContainer.innerHTML = '';
    carouselDots.innerHTML = '';

    carouselItems.forEach((item, index) => {
        const itemDiv = document.createElement('div');
        itemDiv.className = `carousel-item ${index === 0 ? 'active' : ''}`;

        if (item.type === 'photo') {
            itemDiv.innerHTML = `<img src="${item.src}" alt="Memory ${index}">`;
        } else {
            itemDiv.innerHTML = `<video src="${item.src}" autoplay loop muted></video>`;
        }

        carouselContainer.appendChild(itemDiv);

        const dot = document.createElement('div');
        dot.className = `carousel-dot ${index === 0 ? 'active' : ''}`;
        dot.onclick = () => goToCarouselSlide(index);
        carouselDots.appendChild(dot);
    });

    document.getElementById('prevBtn').onclick = () => previousCarouselSlide();
    document.getElementById('nextBtn').onclick = () => nextCarouselSlide();
}

function nextCarouselSlide() {
    currentCarouselIndex = (currentCarouselIndex + 1) % carouselItems.length;
    updateCarousel();
}

function previousCarouselSlide() {
    currentCarouselIndex = (currentCarouselIndex - 1 + carouselItems.length) % carouselItems.length;
    updateCarousel();
}

function goToCarouselSlide(index) {
    currentCarouselIndex = index;
    updateCarousel();
}

function updateCarousel() {
    const items = document.querySelectorAll('.carousel-item');
    const dots = document.querySelectorAll('.carousel-dot');

    items.forEach((item, index) => {
        item.classList.remove('active');
        if (index === currentCarouselIndex) {
            item.classList.add('active');
        }
    });

    dots.forEach((dot, index) => {
        dot.classList.remove('active');
        if (index === currentCarouselIndex) {
            dot.classList.add('active');
        }
    });
}

// Auto-advance carousel
setInterval(nextCarouselSlide, 8000);

// ============ Hero Video ============
function setHeroVideo() {
    const heroVideo = document.getElementById('heroVideo');
    if (videos.length > 0) {
        heroVideo.src = videos[Math.floor(Math.random() * videos.length)];
    }
}

// ============ Photo Gallery ============
function initializePhotoGallery() {
    const photoGrid = document.getElementById('photoGrid');
    photoGrid.innerHTML = '';

    photos.forEach((photo, index) => {
        const photoCard = document.createElement('div');
        photoCard.className = 'photo-card';
        photoCard.style.animationDelay = `${index * 0.05}s`;
        photoCard.innerHTML = `<img src="${photo}" alt="Memory ${index + 1}" loading="lazy">`;
        photoGrid.appendChild(photoCard);
    });

    // Update photo count
    document.getElementById('photoCount').textContent = photos.length;
}

// ============ Video Gallery ============
function initializeVideoGallery() {
    const videoGrid = document.getElementById('videoGrid');
    videoGrid.innerHTML = '';

    videos.forEach((video, index) => {
        const videoCard = document.createElement('div');
        videoCard.className = 'video-card';
        videoCard.style.animationDelay = `${index * 0.05}s`;
        
        const playBtn = document.createElement('div');
        playBtn.className = 'video-play-btn';
        playBtn.innerHTML = '▶️';

        const videoElement = document.createElement('video');
        videoElement.src = video;
        videoElement.preload = 'metadata';

        videoCard.appendChild(videoElement);
        videoCard.appendChild(playBtn);

        // Handle video play/pause
        videoCard.onclick = (e) => {
            e.stopPropagation();
            if (videoElement.paused) {
                videoElement.play();
            } else {
                videoElement.pause();
            }
        };

        videoGrid.appendChild(videoCard);
    });

    // Update video count
    document.getElementById('videoCount').textContent = videos.length;
}

// ============ Random Love Messages ============
function generateMessage() {
    const randomIndex = Math.floor(Math.random() * loveMessages.length);
    const messageElement = document.getElementById('randomMessage');
    const messageBtn = document.querySelector('.btn');

    messageElement.style.opacity = '0';
    messageElement.style.transition = 'opacity 0.3s ease';

    // Add glow effect to button
    if (messageBtn) {
        messageBtn.classList.remove('glow-active');
        // Trigger reflow to restart animation
        void messageBtn.offsetWidth;
        messageBtn.classList.add('glow-active');
        
        // Remove glow class after animation completes
        setTimeout(() => {
            messageBtn.classList.remove('glow-active');
        }, 600);
    }

    setTimeout(() => {
        messageElement.textContent = loveMessages[randomIndex];
        messageElement.style.opacity = '1';
    }, 300);
}

// ============ Floating Hearts ============
function createFloatingHearts() {
    const heart = '❤️';
    const colors = ['#ff69b4', '#ff1493', '#e85b8a', '#c41e5f', '#ff69b4'];

    setInterval(() => {
        const floatingHeart = document.createElement('div');
        floatingHeart.innerHTML = heart;
        floatingHeart.style.position = 'fixed';
        floatingHeart.style.left = Math.random() * window.innerWidth + 'px';
        floatingHeart.style.top = '-30px';
        floatingHeart.style.font = 'bold 20px Arial';
        floatingHeart.style.opacity = '0.7';
        floatingHeart.style.zIndex = '0';
        floatingHeart.style.pointerEvents = 'none';
        floatingHeart.style.animation = 'float-up 4s ease-in forwards';
        floatingHeart.style.color = colors[Math.floor(Math.random() * colors.length)];

        document.body.appendChild(floatingHeart);

        setTimeout(() => floatingHeart.remove(), 4000);
    }, 500);
}

// ============ Particles ============
function createParticles() {
    const particleContainer = document.getElementById('particleContainer');
    const particleCount = 30;

    for (let i = 0; i < particleCount; i++) {
        const particle = document.createElement('div');
        particle.style.position = 'fixed';
        particle.style.width = '4px';
        particle.style.height = '4px';
        particle.style.backgroundColor = ['#ff69b4', '#ff1493', '#e85b8a', '#c41e5f'][Math.floor(Math.random() * 4)];
        particle.style.borderRadius = '50%';
        particle.style.left = Math.random() * 100 + '%';
        particle.style.top = Math.random() * 100 + '%';
        particle.style.opacity = Math.random() * 0.5 + 0.3;
        particle.style.pointerEvents = 'none';
        particle.style.animation = `particle-float ${Math.random() * 3 + 3}s ease-in-out infinite`;
        particle.style.setProperty('--tx', (Math.random() - 0.5) * 200 + 'px');
        particle.style.setProperty('--ty', (Math.random() - 0.5) * 200 + 'px');

        particleContainer.appendChild(particle);
    }
}

// ============ Date Setting ============
function setCurrentDate() {
    const options = { year: 'numeric', month: 'long', day: 'numeric' };
    const currentDate = new Date().toLocaleDateString('en-US', options);
    document.getElementById('currentDate').textContent = `${currentDate} - Made with all my love ❤️`;
}

